def aumentar(valor, taxa):
    aumentar = valor + (valor * (taxa/100))
    return aumentar

def diminuir(valor, taxa):
    diminuir = valor - (valor * (taxa/100))
    return diminuir

def dobro(valor):
    dobro = valor * 2
    return dobro

def metade(valor):
    metade = valor / 2
    return metade
